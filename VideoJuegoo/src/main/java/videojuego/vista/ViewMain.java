private void seleccionarArchivo(){
    this.selectorArchivo.setAcceptAllFileFilterUsed(false);
    this.selectorArchivo.addChoosableFileFilter(this.filtroArchicoCSV);
    this.selectorArchivo.showOpenDialog(this);
    try{
        this.rutaArchivo.setText(this.selectorArchivo.getSelectedFile().getAbsolutePath());
    }catch(NullPointerException nfe)
            {
                
                JOptionPane.showMessageDialog(null,"No se cargo ningun archivo, intente de nuevo", "Archivo no seleccionado",JOptionPane.WARNING_MESSAGE);
            }    
}

